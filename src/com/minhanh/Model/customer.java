package com.minhanh.Model;

public class customer {
	
	private int customer_id;
	private String customer_indentity_card;
	private String customer_fullname;
	private String customer_address;
	private String customer_phone;
	private String customer_birthday;
	private String customer_notes;
	private String customer_email;
	private String customer_office;
	private String customer_sex;
	private String customer_isative;
	private String customer_level;
	private String customer_company;
	private String customer_code;
	public customer() {
		
	}
	public customer(int customer_id, String customer_indentity_card, String customer_fullname, String customer_address,
			String customer_phone, String customer_birthday, String customer_notes, String customer_email,
			String customer_office, String customer_sex, String customer_isative, String customer_level,
			String customer_company, String customer_code) {
		super();
		this.customer_id = customer_id;
		this.customer_indentity_card = customer_indentity_card;
		this.customer_fullname = customer_fullname;
		this.customer_address = customer_address;
		this.customer_phone = customer_phone;
		this.customer_birthday = customer_birthday;
		this.customer_notes = customer_notes;
		this.customer_email = customer_email;
		this.customer_office = customer_office;
		this.customer_sex = customer_sex;
		this.customer_isative = customer_isative;
		this.customer_level = customer_level;
		this.customer_company = customer_company;
		this.customer_code = customer_code;
	}
	public int getCustomer_id() {
		return customer_id;
	}
	public void setCustomer_id(int customer_id) {
		this.customer_id = customer_id;
	}
	public String getCustomer_indentity_card() {
		return customer_indentity_card;
	}
	public void setCustomer_indentity_card(String customer_indentity_card) {
		this.customer_indentity_card = customer_indentity_card;
	}
	public String getCustomer_fullname() {
		return customer_fullname;
	}
	public void setCustomer_fullname(String customer_fullname) {
		this.customer_fullname = customer_fullname;
	}
	public String getCustomer_address() {
		return customer_address;
	}
	public void setCustomer_address(String customer_address) {
		this.customer_address = customer_address;
	}
	public String getCustomer_phone() {
		return customer_phone;
	}
	public void setCustomer_phone(String customer_phone) {
		this.customer_phone = customer_phone;
	}
	public String getCustomer_birthday() {
		return customer_birthday;
	}
	public void setCustomer_birthday(String customer_birthday) {
		this.customer_birthday = customer_birthday;
	}
	public String getCustomer_notes() {
		return customer_notes;
	}
	public void setCustomer_notes(String customer_notes) {
		this.customer_notes = customer_notes;
	}
	public String getCustomer_email() {
		return customer_email;
	}
	public void setCustomer_email(String customer_email) {
		this.customer_email = customer_email;
	}
	public String getCustomer_office() {
		return customer_office;
	}
	public void setCustomer_office(String customer_office) {
		this.customer_office = customer_office;
	}
	public String getCustomer_sex() {
		return customer_sex;
	}
	public void setCustomer_sex(String customer_sex) {
		this.customer_sex = customer_sex;
	}
	public String getCustomer_isative() {
		return customer_isative;
	}
	public void setCustomer_isative(String customer_isative) {
		this.customer_isative = customer_isative;
	}
	public String getCustomer_level() {
		return customer_level;
	}
	public void setCustomer_level(String customer_level) {
		this.customer_level = customer_level;
	}
	public String getCustomer_company() {
		return customer_company;
	}
	public void setCustomer_company(String customer_company) {
		this.customer_company = customer_company;
	}
	public String getCustomer_code() {
		return customer_code;
	}
	public void setCustomer_code(String customer_code) {
		this.customer_code = customer_code;
	}
	
	
}
